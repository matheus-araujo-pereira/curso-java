// Crie um programa Java que demonstre o valor padrão de variáveis primitivas (int, double, char, boolean) quando não inicializadas explicitamente.

public class Questao03 {

    static int inteiro;
    static double numeroDouble;
    static char character;
    static boolean booleano;

    public static void main(String[] args) {

        System.out.println("Valor padrão de int: " + inteiro);
        System.out.println("Valor padrão de double: " + numeroDouble);
        System.out.println("Valor padrão de char: " + character);
        System.out.println("Valor padrão de boolean: " + booleano);
    }
}
