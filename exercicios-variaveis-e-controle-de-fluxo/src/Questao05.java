// Escreva um programa Java que converta graus Celsius para Fahrenheit.

public class Questao05 {

    public static void main(String[] args) {

        double temperaturaEmGrausCelsius;
        double temperaturaEmFahrenheit;

        temperaturaEmGrausCelsius = 24;
        temperaturaEmFahrenheit = (temperaturaEmGrausCelsius * 1.8) + 32;

        System.out.println(temperaturaEmFahrenheit);
    }
}
