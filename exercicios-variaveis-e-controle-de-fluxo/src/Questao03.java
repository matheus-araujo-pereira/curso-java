// Escreva um programa Java que calcule a média de três números inteiros.

public class Questao03 {

    public static void main(String[] args) {

        int primeiroNumero;
        int segundoNumero;
        int terceiroNumero;
        int media;

        primeiroNumero = 10;
        segundoNumero = 5;
        terceiroNumero = 0;

        media = (primeiroNumero + segundoNumero + terceiroNumero) / 3;

        System.out.println(media);
    }
}
